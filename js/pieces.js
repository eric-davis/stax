export class Bag {
  constructor() {
    this._bag = [];
    this._refill();
  }

  _refill() {
    this._bag = ['I','J','L','O','S','T','Z'];
    for (let i = this._bag.length - 1; i > 0; i--) {
      const j = Math.floor(Math.random() * (i + 1));
      [this._bag[i], this._bag[j]] = [this._bag[j], this._bag[i]];
    }
  }

  next() {
    if (this._bag.length === 0) this._refill();
    return this._bag.pop();
  }
}
